package dailyExercise.LeetCodeListNode;

/**
 * @ClassName: ListNode
 * @Description:
 * @author: dk
 * @date: 2021/12/21 3:57 下午
 */

public class ListNode {

    int val;
    ListNode next;


    ListNode() {
    }


    ListNode( int val ) {
        this.val = val;
    }


    ListNode( int val, ListNode next ) {
        this.val = val;
        this.next = next;
    }


}
